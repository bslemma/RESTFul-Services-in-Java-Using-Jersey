package com.mum.model;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class User {
	private String Id;
	private String name;
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
